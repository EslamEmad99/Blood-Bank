package eslam.emad.bloodbank.data.models;

public class ExampleCheckBox {

    private String text;
    private int id;

    public ExampleCheckBox(String text, int id) {
        this.text = text;
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public int getId() {
        return id;
    }
}
